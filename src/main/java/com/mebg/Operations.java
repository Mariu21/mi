package com.mebg;

public class Operations {
    public int add(int a, int b) {
        return a + b;
    }

    public int rest(int a, int b) {
        return a - b;
    }


    public int multiply(int a, int b) {
        return a * b;
    }

    public int divide(int a, int b) {
        return a / b;
    }




}
